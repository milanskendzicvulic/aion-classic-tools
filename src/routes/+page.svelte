<h1>AionClassicTools</h1>
<p>A set of tools for your everyday requirements.</p>

<h2>Features</h2>
<ul>
	<li>
		<strong>Enchantment Calculator</strong>: Calculate the success rates of enchantment stones,
		considering factors such as enchantment level, rarity, and item level.
	</li>
	<li>
		<strong>Enchantment Simulator</strong> (Coming Soon): Determine the average number of stones
		required to reach a specific enchantment level, factoring in item rarity and level.
	</li>
	<li>
		<strong>Time Table</strong> (Coming Soon): Stay informed about the timing of various in-game
		events.
	</li>
</ul>

